    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String databaseURL = "jdbc:mysql://localhost/student_management_system";
        String username = "root";
        String password = "root";

        // Get data from JTextFields
        String updatedStudentID = updatebar.getText();  // Get the ID from
        String newStudentID = stuid.getText();       // Get the new ID from
        String updatedStudentName = stuname.getText();
        String updatedcourse = course.getText();

        try ( Connection connection = DriverManager.getConnection(databaseURL, username, password)) {

            // First, update the ID (if it's changed)
            if (!updatedStudentID.equals(newStudentID)) {
                String sqlUpdateID = "UPDATE student SET Student_id=? WHERE Student_id=?";
                PreparedStatement preparedStatementUpdateID = connection.prepareStatement(sqlUpdateID);
                preparedStatementUpdateID.setString(1, newStudentID);
                preparedStatementUpdateID.setString(2, updatedStudentID);
                int rowsUpdatedID = preparedStatementUpdateID.executeUpdate();

                if (rowsUpdatedID > 0) {
                    JOptionPane.showMessageDialog(this, "Student ID updated successfully.");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update Student ID.");
                    return;
                }
            }

            // Then, update name and department
            String sqlUpdateData = "UPDATE student SET Student_name=?, Course=? WHERE Student_id=?";
            PreparedStatement preparedStatementUpdateData = connection.prepareStatement(sqlUpdateData);
            preparedStatementUpdateData.setString(1, updatedStudentName);
            preparedStatementUpdateData.setString(2, updatedcourse);
            preparedStatementUpdateData.setString(3, newStudentID);
            int rowsUpdatedData = preparedStatementUpdateData.executeUpdate();

            if (rowsUpdatedData > 0) {
                JOptionPane.showMessageDialog(this, "Student data updated successfully.");

                // You can clear the JTextFields here if needed.
                stuid.setText("");
                stuname.setText("");
                course.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "No Student found with the specified ID.");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

