    private void updatebarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_updatebarKeyReleased
       String databaseURL = "jdbc:mysql://localhost/student_management_system";
        String username = "root";
        String password = "root";

        // Get the ID entered in jTextField4
        String studentIDToFetch = updatebar.getText();  // Assuming jTextField4 is declared and initialized

        try (Connection connection = DriverManager.getConnection(databaseURL, username, password)) {

            String sql = "SELECT * FROM student WHERE Student_id=?";
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, studentIDToFetch);

            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                String student_id = resultSet.getString("Student_id");
                String student_name = resultSet.getString("Student_name");
                String coursev = resultSet.getString("Course");

                // Display the data in respective JTextFields
                stuid.setText(student_id);
                stuname.setText(student_name);
                course.setText(coursev);

            } else {
                stuid.setText("");
                stuname.setText("");
                course.setText("");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_updatebarKeyReleased

